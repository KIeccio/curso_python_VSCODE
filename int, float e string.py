

"""
Tipos int e float
int --> número inteiro
O tipo int representa qualquer número positivo ou 
negativo

"""

print(11) #int
print(-11) #int
print(0) #int

"""
float --> número com ponto flutuante
O tipo float representa qualquer número positivo
ou negativo com ponto flutuante.

"""

print(1.1) #float
print(-1.1) #float
print(0.0) #float

"""
A função type mostra o tipo que o Python inferiu
o valor:

"""

print(     type('Kleccio')     )
print(     type(    0    )     )
print(     type(   1.1   )     )



